/**
 * プロフィールページの掲載内容。
 * 設定として確定している事実のみを載せる。未確定の内容は補完しない。
 */

/**
 * タップして少しずつ知る「すぴたろうのこと」1 項目。
 * 確定情報のみを整理して載せる(未確定の性格・特技・好みなどは創作しない)。
 * 将来複数キャラクター対応へ移す場合も、この形のまま流用できる程度に留める。
 */
export type ProfileTrait = {
  /** 安定した英数字の ID(React key・状態管理に使う) */
  id: string;
  /** カード見出し(短い日本語の問いかけ) */
  label: string;
  /** カード上に出す短い要約(2〜3 行以内) */
  summary: string;
  /** 詳細本文(ボトムシート / No-JS 時のインライン全文)。確定情報だけで構成する。 */
  detail: string;
};

export type Profile = {
  name: string;
  /** 短いキャラクター紹介(1 行ずつ表示する) */
  intro: string[];
  /** タップで詳しく読めるプロフィール項目。確定情報のみ。 */
  traits: ProfileTrait[];
  /** すぴたろうの独自の言葉。人間には完全には分からないため、意味の解説は付けない。 */
  words: string[];
  /** 将来追加予定の詳細設定。確定したらここに追記する。空の間は「準備中」を表示する。 */
  upcoming: string[];
};

